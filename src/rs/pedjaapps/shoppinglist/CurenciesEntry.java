package rs.pedjaapps.shoppinglist;

public final class CurenciesEntry
{


	private final double eur;
	private final double gbp;
	private final double hrk;
	private final double huf;
	private final double jpy;
	private final double kwd;
	private final double nok;
	private final double sek;
	private final double usd;
	private final double dkk;
	private final double czk;
	private final double chf;
	private final double cad;
	private final double bam;
	private final double aud;
	
	public CurenciesEntry(double eur, double gbp, double hrk, double huf,
			double jpy, double kwd, double nok, double sek, double usd,
			double dkk, double czk, double chf, double cad, double bam,
			double aud) {
		super();
		this.eur = eur;
		this.gbp = gbp;
		this.hrk = hrk;
		this.huf = huf;
		this.jpy = jpy;
		this.kwd = kwd;
		this.nok = nok;
		this.sek = sek;
		this.usd = usd;
		this.dkk = dkk;
		this.czk = czk;
		this.chf = chf;
		this.cad = cad;
		this.bam = bam;
		this.aud = aud;
	}

	public double getEur() {
		return eur;
	}

	public double getGbp() {
		return gbp;
	}

	public double getHrk() {
		return hrk;
	}

	public double getHuf() {
		return huf;
	}

	public double getJpy() {
		return jpy;
	}

	public double getKwd() {
		return kwd;
	}

	public double getNok() {
		return nok;
	}

	public double getSek() {
		return sek;
	}

	public double getUsd() {
		return usd;
	}

	public double getDkk() {
		return dkk;
	}

	public double getCzk() {
		return czk;
	}

	public double getChf() {
		return chf;
	}

	public double getCad() {
		return cad;
	}

	public double getBam() {
		return bam;
	}

	public double getAud() {
		return aud;
	}

	
	


}
